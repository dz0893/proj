	public static string GetDamageTypeName(DamageType type)
	{
		if (type == DamageType.Physical)
			return physical;
		else if (type == DamageType.Piercing)
			return piercing;
		else if (type == DamageType.Magical)
			return magical;
		else if (type == DamageType.Siege)
			return siege;
		else
			return trueDamage;
	}
	
	public static string GetSignedValue(int value)
	{
		if (value > 0)
			return "+" + value;
		else
			return value + "";
	}

